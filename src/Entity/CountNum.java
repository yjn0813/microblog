package Entity;

public class CountNum {
	private String article_num;
	private String follow_num;
	private String fan_num;
	private String ID;
	public String getArticle_num() {
		return article_num;
	}
	public void setArticle_num(String articleNum) {
		article_num = articleNum;
	}
	public String getFollow_num() {
		return follow_num;
	}
	public void setFollow_num(String followNum) {
		follow_num = followNum;
	}
	public String getFan_num() {
		return fan_num;
	}
	public void setFan_num(String fanNum) {
		fan_num = fanNum;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	
}
