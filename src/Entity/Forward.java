package Entity;

public class Forward {
	private String forward_id;
	private String forward_content;
	private String article_id;
	private String ID;
	private String photo;
	private String article_content;
	private String articlePic;
	
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	public String getArticle_content() {
		return article_content;
	}
	public void setArticle_content(String articleContent) {
		article_content = articleContent;
	}
	public String getArticlePic() {
		return articlePic;
	}
	public void setArticlePic(String articlePic) {
		this.articlePic = articlePic;
	}
	public String getForward_id() {
		return forward_id;
	}
	public void setForward_id(String forwardId) {
		forward_id = forwardId;
	}
	public String getForward_content() {
		return forward_content;
	}
	public void setForward_content(String forwardContent) {
		forward_content = forwardContent;
	}
	public String getArticle_id() {
		return article_id;
	}
	public void setArticle_id(String articleId) {
		article_id = articleId;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
}
